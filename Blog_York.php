<div class="main_text_2">


	Yes, the moment we start travelling.<br>
	
	I assume that a train has a mysterious power to make people isolate themselves. All passengers share the space together, but they completely become alone. It reminds me of home at that moment I switch moods to jump into an unknown world. Ironically, we might think about home when we’ve just left our home rather than while we are at home. It does not matter whether the journey is short or longer.<br><br>
	
	What is HOME made of? Family? Friends? or Safe place?<br><br>
	
	Many interviewees answered with these words for my question, “what does home mean to you”. Nevertheless It is still hard to pick an accurate word to describe it, but it could be ‘somewhere’ to go back from at least a short trip for sure. Such as ‘Home sweet home’. Because unknown cities bring very spontaneous stimulation to remind the people of any kind of their own familiar sights.<br>
	
	However, what if the journey would be longer? What if the people decide not to come back. How could we meet ‘the somewhere’ from the unknown cities?<br><br><br>
	
