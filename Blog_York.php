	It was a changeable weather as usual in England, was a perfect day for my hoodie. When a sky turned into a pastel colour tone of blue with a pure white cotton candy, I met a first interviewee in Deans Park, York.<br>
	
	A lovely girl I spoke for the video above, she was hiding half of her body behind her family. I asked her ‘when do you miss home the most?’, ‘have you ever missed home?’ then she answered,<br>
	
	‘When I was on a train’.<br>
	
	I truly believe that all generations could have a strong empathy with the young lady’s answer. It was absolutely clear to realise the point when we start thinking about Home.<br>
	
	The city, York is a completely gorgeous town for a one day picnic. My first visit had quite tight schedule to enjoy York International Short Film Festival, so I needed to plan for the second visit. The town has a lot of precious spots to look around, especially The Shamble, one of oldest streets in York, is famous for a representative location of a series film, Harry Potter.<br>

